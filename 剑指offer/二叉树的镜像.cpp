/*
题目描述
操作给定的二叉树，将其变换为源二叉树的镜像。
输入描述:
二叉树的镜像定义：源二叉树 
    	    8
    	   /  \
    	  6   10
    	 / \  / \
    	5  7 9 11
    	镜像二叉树
    	    8
    	   /  \
    	  10   6
    	 / \  / \
    	11 9 7  5

*/

/*
struct TreeNode {
	int val;
	struct TreeNode *left;
	struct TreeNode *right;
	TreeNode(int x) :
			val(x), left(NULL), right(NULL) {
	}
};*/
class Solution {
public:
    void Mirror(TreeNode *pRoot)
    {
        TreeNode *p=pRoot;
        if (p==NULL)
            return;
        if (p->left==NULL&&p->right==NULL)
            return;
        TreeNode *temp=p->left;
        p->left=p->right;
        p->right=temp;
        if (p->left) 
            Mirror(p->left);
        if (p->right)
            Mirror(p->right);
        
    }
};